let currentLang = localStorage.getItem('ng_lang') || 'en';

function setLang(lang) {
  currentLang = lang;
  localStorage.setItem('ng_lang', lang);
  applyLang();
  // Close dropdown
  document.getElementById('langDropdown').classList.remove('open');
}

function applyLang() {
  const t = translations[currentLang];
  const dir = currentLang === 'fa' || currentLang === 'ar' ? 'rtl' : 'ltr';
  document.documentElement.lang = currentLang;
  document.body.dir = dir;
  document.querySelector('html').dir = dir;

  // Update label
  const labels = { en: 'EN', fa: 'FA', ar: 'AR' };
  document.querySelector('.lang-label').textContent = labels[currentLang];

  // Translate all elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key]) el.textContent = t[key];
  });

  // Placeholders
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (t[key]) el.placeholder = t[key];
  });
}

// Init on load
document.addEventListener('DOMContentLoaded', () => { applyLang(); });
